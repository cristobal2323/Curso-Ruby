my_hash = { } 
my_hash["name"] = "Dana"
my_hash["age"] = 34 
my_hash["eye_color"] = "green"
#this will populate your hash item by item
my_hash = {
	"name" => "Dana", 
	"age" => 34,
	"eye_color" => "green" }
my_hash = { :name => "Dana", :age => 34, :eye_color => "green"}
#you can use the colon in front of the key value to enter information into a hash as well
puts my_hash